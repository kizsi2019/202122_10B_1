
    # int típus egész szám tárolására
    szam = 5
    print(type(szam))

    # float típus tizedes tört tárolására
    tizedes_tort = 0.25
    print(type(tizedes_tort))

    # str típus (string) karaktersorozat tárolására
    szoveg = 'alma'
    print(type(szoveg))

    # bool típus (boolean) két értéket vehet fel: True vagy False
    oszthato = True
    print(type(oszthato))
  